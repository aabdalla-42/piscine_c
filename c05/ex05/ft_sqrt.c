int		ft_sqrt(int nb)
{
	int i;
	int r;

	r = 1;
	i = 1;
	while (r <= nb / 2)
	{
		i = r * r;
		if (i == nb)
		{
			return (r);
		}
		r++;
	}
	return (0);
}
